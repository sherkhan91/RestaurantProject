package com.example.sher.restaurantproject.Models;

public class NearbyHotelModel {
    String restaurant_name;
    String location;
    String food_country;
    String rating;
    String review_count;
    String image_reference;

    public String getRestaurant_name() {
        return restaurant_name;
    }

    public void setRestaurant_name(String restaurant_name) {
        this.restaurant_name = restaurant_name;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getFood_country() {
        return food_country;
    }

    public void setFood_country(String food_country) {
        this.food_country = food_country;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public String getReview_count() {
        return review_count;
    }

    public void setReview_count(String review_count) {
        this.review_count = review_count;
    }

    public String getImage_reference() {
        return image_reference;
    }

    public void setImage_reference(String image_reference) {
        this.image_reference = image_reference;
    }


}
